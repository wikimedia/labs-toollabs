<h1>Wikimedia Tool Labs</h1>
<p><strong>Tool Labs</strong> (or <strong>Tools</strong>) is a hosting environment for community developers working on tools and bots that help users maintain and use Wikimedia wikis. Tool Labs provides access to <a href="https://wikitech.wikimedia.org/wiki/Help:Tool_Labs/Database">replicas of Wikimedia databases</a> and other services that allow developers to easily compute analytics, do bot work, and create tools to help editors and other volunteers in their work. The infrastructure is supported by a dedicated <a href="https://wikitech.wikimedia.org/wiki/Nova_Resource:Tools">group of Wikimedia Foundation staff and volunteers.</a></p>

<p>Read more about <a href="https://wikitech.wikimedia.org/wiki/Portal:Tool_Labs">Tool Labs on wikitech (the Wikimedia technical infrastructure wiki)</a>.

